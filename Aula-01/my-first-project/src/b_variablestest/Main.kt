package b_variablestest

fun main() {
    println("Analyzing Variable Concepts")

    // Example Kotlin variable
    var variable1: Int
    // Impossible attributing String on variable counter, because your type is Int, not String.
    variable1 = 10

    // ------------------------------------------------------------------------------------------- //

    // Initialization new variable and attributing new value
    var variable2: Int = 20

    // Initialization new variable - It identifies that it's INT
    var variable3 = 30

    // Changing variable value
    variable3 = 50

    // Variable that does not allow reassignment - YOU CAN NOT CHANGE IT
    val variable4 = 100




}